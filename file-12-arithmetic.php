<?php 

/** 
 * Arithmetic Operators
 * +    Addition (2+3 = 5)
 * -    Subtraction (2-3 = -1)
 * *    Multiplication (2*3 = 6)
 * /    Division (2/3 = 0.66666666666667)
 * %    Modulus (2%3 = 2)
 * **   Exponentiation  (2^3 = 8)
 * ++   Increment (2++ = 3)
 * --   Decrement (2-- = 1)
 */


$number1 = 10;
$number2 = 20;

$result = $number1 + $number2;
echo "Result 1 is ". $result;
echo "</br>";
$result2 = $number1 - $number2;
echo "Result 2 is ". $result2;
echo "</br>";
$result3 = $number1 * $number2;
echo "Result 3 is ". $result3;
echo "</br>";
$result4 = $number1 / $number2;
echo "Result 4 is ". $result4;
echo "</br>";
$result5 = $number1 % $number2;
echo "Result 5 is ". $result5;
echo "</br>";
$result6 = $number1 ** $number2;
echo "Result 6 is ". $result6;
echo "</br>";
$result7 = $number1++;
echo "Result 7 is ". $result7;
echo "</br>";
$result8 = ++$number1;
echo "Result 8 is ". $result8;
echo "</br>";
$result9 = $number1--;
echo "Result 9 is ". $result9;
echo "</br>";
$result10 = --$number1;
echo "Result 10 is ". $result10;
echo "</br>";
